        $(document).on('click', '.js-delete-record', function() {
            $('#js-confirm-delete-record').attr('data-url', $(this).data('url'))
            $('#js-confirm-delete-record').attr('data-id', $(this).data('id'))
        });

        $(document).on('click', '#js-confirm-delete-record', function(e) {
            e.preventDefault();
            var url = $('#js-confirm-delete-record').data('url')
            var id = $('#js-confirm-delete-record').data('id')
            var token = $('meta[name=csrf-token]').attr('content')
            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': token
                }
            });
            $.ajax({
                url: url,
                method: 'delete',
                data: {
                    id: id,
                },
                success: function (response) {
                    document.location.reload();
                },
                error: function (response) {
                    document.location.reload();
                }
            });
        });
    });
